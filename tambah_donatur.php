    $nama = mysqli_real_escape_string($conn, $_POST['nama']);
    $email = mysqli_real_escape_string($conn, $_POST['email']);
    $password = mysqli_real_escape_string($conn, $_POST['password']);

    $check_email = mysqli_query($conn, "SELECT id FROM users WHERE email = '$email'");
    if (mysqli_num_rows($check_email) > 0) {
        echo "<script>alert('Email sudah digunakan! Silakan gunakan email lain.');</script>";
    } else {
        $hashed_password = password_hash($password, PASSWORD_DEFAULT);
        
        $insert_query = "INSERT INTO users (nama, email, password, role) VALUES ('$nama', '$email', '$hashed_password', 'donatur')";

        if (mysqli_query($conn, $insert_query)) {
            echo "<script>alert('Donatur berhasil ditambahkan!'); window.location='data_donatur.php';</script>";
        } else {
            echo "<script>alert('Gagal menambahkan donatur!');</script>";
        }
    }
